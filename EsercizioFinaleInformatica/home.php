            <div class="servizio">
                <h3>Sistemi di Gioco</h3>
                <p>
                    Il futsal moderno si basa su tre moduli principali: il <b>3-1</b> (con Pivot fisso che fa salire la squadra), il <b>4-0</b> (sistema dinamico senza ruoli fissi per disorientare la difesa) e il <b>2-2</b> (usato raramente, tipico del calcio a 5 delle origini). La rotazione continua è la chiave: ogni giocatore deve saper coprire ogni zona del campo in meno di 3 secondi.
                </p>
                <div class="infoExtra">
                    <b>Consiglio:</b> in fase di non possesso, la difesa a "U" è la più efficace contro il 4-0.
                </div>
            </div>

            <div class="servizio">
                <h3>Il Pallone a "Rimbalzo Controllato"</h3>
                <p>
                    Perché il pallone da Futsal non rimbalza come quello da calcio? La sfera (misura 4) contiene all'interno uno strato di schiuma o ovatta che ne riduce l'elasticità. Questo permette scambi rapidissimi e un controllo di suola millimetrico. <b>Nota bene:</b> Se il pallone finisce fuori, la rimessa va effettuata rigorosamente con i piedi entro 4 secondi.
                </p>
                <div class="infoExtra">
                    <b>Errore comune:</b> calciare la rimessa direttamente in rete non vale gol.
                </div>
            </div>

            <div class="servizio">
                <h3>Origini e Nome</h3>
                <p>
                    Nato nel 1930 a Montevideo (Uruguay), il gioco era inizialmente chiamato <i>Fútbol de Salón</i>. Il termine "Futsal" è nato solo nel 1985 a Madrid, dall'unione delle parole spagnole <b>FUT</b>bol e <b>SAL</b>on. Fu scelto per distinguere ufficialmente lo sport gestito dalla FIFA dalla AMF.
                </p>
                <div class="infoExtra">
                     <b>Curiosità:</b> la prima partita fu giocata 5 contro 5 usando porte da pallamano.
                </div>
            </div>

            <div class="servizio">
                <h3>Il Tempo Effettivo</h3>
                <p>
                    A differenza del calcio a 11, nel Futsal professionistico si gioca con il <b>tempo effettivo</b>: il cronometro si ferma ogni volta che la palla esce. Le partite durano due tempi da 20 minuti. Inoltre, ogni allenatore ha a disposizione un <b>Time-out</b> di un minuto per tempo.
                </p>
                <div class="infoExtra">
                     <b>Lo sapevi?</b> un giocatore ha solo 4 secondi per riprendere il gioco.
                </div>
            </div>

            <div class="servizio">
                <h3>Il Portiere di Movimento</h3>
                <p>
                    Quando una squadra è in svantaggio, può sostituire il portiere con un giocatore di movimento. Questo crea una situazione di <b>5 contro 4</b> in attacco. È una tattica rischiosissima: basta un errore per subire un gol a porta vuota dalla lunga distanza.
                </p>
                <div class="infoExtra">
                    <b>Regola:</b> il portiere non può toccare la palla nella propria metà campo per più di 4 secondi.
                </div>
            </div>

            <div class="servizio">
                <h3>Fabbrica di Talenti</h3>
                <p>
                    Moltissime leggende come <b>Ronaldinho, Neymar e Messi</b> attribuiscono la loro tecnica nello stretto proprio a questo sport. Ricardinho e Falcão sono invece considerati i "GOAT" assoluti della disciplina, capaci di giocate incredibili.
                </p>
                <div class="infoExtra">
                    <b>Citazione:</b> "Il Futsal mi ha aiutato a controllare meglio la palla." — Leo Messi.
                </div>
